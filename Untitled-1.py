# Пример простой квест-игры на Python

def start_game():
    print("Добро пожаловать в квест-игру!")
    print("Вы находитесь в комнате. Перед вами две двери: одна справа, другая слева.")
    print("Куда вы хотите пойти? (напишите 'вправо' или 'влево')")

    choice = input("> ").lower()

    if choice == "вправо":
        right_door()
    elif choice == "влево":
        left_door()
    else:
        print("Пожалуйста, выберите 'вправо' или 'влево'.")
        start_game()

def right_door():
    print("Вы открываете правую дверь и видите сундук с сокровищами!")
    print("Поздравляем! Вы выиграли!")

def left_door():
    print("Вы зашли в левую дверь и оказались в темной комнате.")
    print("Что вы хотите сделать?")
    print("1. Искать выход")
    print("2. Вернуться обратно")

    choice = input("> ")

    if choice == "1":
        print("Вы нашли выход! Поздравляем!")
    elif choice == "2":
        start_game()
    else:
        print("Пожалуйста, введите 1 или 2.")
        left_door()

# Начало игры
start_game()
